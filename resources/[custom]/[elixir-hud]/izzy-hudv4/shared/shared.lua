    getStatus = function(statIndex)
        local pPed = PlayerPedId()
        local pId = PlayerId()
        if statIndex == 'health' then
            return GetEntityHealth(pPed) > 0 and (GetEntityHealth(pPed) - 100) or 0
        elseif statIndex == 'armor' then
            return GetPedArmour(pPed)
        elseif statIndex == 'stamina' then
            if not IsEntityInWater(pPed) then
                return 100 - GetPlayerSprintStaminaRemaining(pId)
            elseif IsEntityInWater(pPed) then
                return GetPlayerUnderwaterTimeRemaining(pId) * 10
            end
        elseif statIndex == 'mic' then
            local voiceState = getVoiceState()
            return cfg.voiceRanges[tostring(voiceState.range)] or 33
        elseif statIndex == 'stress' then
            return current.stress
        else
            if framework == 'qb' then
                return QBCore.Functions.GetPlayerData().metadata[statIndex]
            elseif framework == 'esx' then
                return stats[statIndex] or 0
            end
        end
    end

    getVoiceState = function()
        if GetResourceState('pma-voice') == 'started' then
            local range = cfg.voiceRanges[tostring(LocalPlayer.state['proximity']?.distance or 66)]
            return { talking = NetworkIsPlayerTalking(PlayerId()), range = LocalPlayer.state['proximity']?.distance or 0, rangeLabel = cfg.rangeLabels[tostring(range)] or cfg.rangeLabels["66"] }
        end

        return { talking = NetworkIsPlayerTalking(PlayerId()), range = 1.5, rangeLabel = cfg.rangeLabels["66"] }
    end

    getTopInfo = function(data)
        if data.id == "fullname" then
            if framework == "qb" then
                if QBCore and QBCore.Functions.GetPlayerData() then
                    data.value = ("%s %s"):format(QBCore.Functions.GetPlayerData().charinfo.firstname, QBCore.Functions.GetPlayerData().charinfo.lastname)
                    data.secondValue = ("(%s)"):format(GetPlayerServerId(PlayerId()))
                end
            elseif framework == "esx" then
                if ESX and ESX.GetPlayerData() then
                    data.value = ("%s %s"):format(ESX.GetPlayerData().firstName, ESX.GetPlayerData().lastName)
                    data.secondValue = ("(%s)"):format(GetPlayerServerId(PlayerId()))
                end
            end
        elseif data.id == "cash" then
            if framework == 'qb' then
                if QBCore and QBCore.Functions.GetPlayerData() and QBCore.Functions.GetPlayerData().money then
                    data.value = QBCore.Functions.GetPlayerData().money['cash']
                end
            elseif framework == 'esx' then
                if ESX and ESX.GetPlayerData() then
                    if ESX.GetPlayerData().accounts and ESX.GetPlayerData().accounts['cash'] then
                        data.value = ESX.GetPlayerData().accounts['cash']
                    else
                        data.value = ESX.GetAccount("money").money
                    end
                end
            end
        elseif data.id == "bank" then
            if framework == 'qb' then
                if QBCore and QBCore.Functions.GetPlayerData() and QBCore.Functions.GetPlayerData().money then
                    data.value = QBCore.Functions.GetPlayerData().money['bank']
                end
            elseif framework == 'esx' then
                if ESX and ESX.GetPlayerData() then
                    if ESX.GetPlayerData().accounts and ESX.GetPlayerData().accounts['bank'] then
                        data.value = ESX.GetPlayerData().accounts['bank']
                    else
                        data.value = ESX.GetAccount("bank").money
                    end
                end
            end
        elseif data.id == "job" then
            if framework == "qb" then
                if QBCore and QBCore.Functions.GetPlayerData() and QBCore.Functions.GetPlayerData().job then
                    data.value = QBCore.Functions.GetPlayerData().job.label
                end
            elseif framework == "esx" then
                if ESX and ESX.GetPlayerData() and ESX.GetPlayerData().job then
                    data.value = ESX.GetPlayerData().job.label
                end
            end
        end

        return data
    end

